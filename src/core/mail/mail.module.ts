import { MailerModule } from '@nestjs-modules/mailer';
import { HandlebarsAdapter } from '@nestjs-modules/mailer/dist/adapters/handlebars.adapter';
import { Module } from '@nestjs/common';
import { ConfigService } from '@nestjs/config';
import { join } from 'path';
import { HelperModule } from '../helpers/helper.module';
import { MailService } from './mail.service';

@Module({
  imports: [
    HelperModule,
    MailerModule.forRootAsync({
      useFactory: async (configService: ConfigService) => ({
        transport: {
          host: configService.get('mail.mailHost'),
          secure: configService.get('mail.mailSecure'),
          port: configService.get('mail.mailPort'),
          auth: {
            user: configService.get('mail.defaultMailUser'),
            pass: configService.get('mail.defaultMailPassword'),
          },
        },
        preview: false,
        template: {
          dir: join(__dirname, 'templates'),
          adapter: new HandlebarsAdapter(),
          options: {
            strict: true,
          },
        },
      }),
      inject: [ConfigService],
    }),
  ],
  providers: [MailService],
  exports: [MailService],
})
export class MailModule {}
